    /**
     * 对map排序并且转成字符串
     * @param oriMap
     * @return
     */
    public static String sortMapByKeyToString(Map<String, String> oriMap) {
        if (oriMap == null || oriMap.isEmpty()) {
            return null;
        }
        List<Map.Entry<String, String>> infoIds = new ArrayList<Map.Entry<String, String>>(oriMap.entrySet());

        //排序方法
        Collections.sort(infoIds, (o1, o2) -> {
            //return (o2.getValue() - o1.getValue());
            return (o1.getKey()).toString().compareTo(o2.getKey());
        });

        StringBuilder sb = new StringBuilder();
        String eq = "=";
        String and = "&";

        for (Map.Entry<String, String> infoId : infoIds) {
            sb.append(infoId.getKey()).append(eq).append(infoId.getValue()).append(and);
        }

        return sb.toString().substring(0, sb.length() - 1);
    }

